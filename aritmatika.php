<?php

$angka1 = 100; //integer
$angka2 = 10;
//addition penjumlahan
$result = $angka1 + $angka2;

echo "penjumlahan dari" . $angka1 .' + '. $angka2 . ' = ' .$result.PHP_EOL;
//PHP_EOL : garis baru atau new line

//subtraction pengurangan
$pengurangan = $angka1 - $angka2; 

echo "pengurangan dari" . $angka1 . ' - '. $angka2 . ' = ' .$pengurangan.PHP_EOL;

//multiplication perkalian
$perkalian = $angka1 * $angka2;

echo "perkalian dari" . $angka1 . ' * '. $angka2 . ' = ' .$perkalian.PHP_EOL;

